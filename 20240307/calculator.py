def add(a, b):
    """더하기 함수
    Args:
        a (int): 첫 번째 숫자
        b (int): 두 번째 숫자
    Returns:
        int : 계산 결과
    
    >>> add(10, 2)
    12
    """
    return a + b

def sub(a, b):
    """빼기 함수
    Args:
        a (int): 첫 번째 숫자
        b (int): 두 번째 숫자
    Returns:
        int : 계산 결과
    
    >>> sub(10, 2)
    8
    """
    return a - b

def mul(a, b):
    """곱하기 함수
    Args:
        a (int): 첫 번째 숫자
        b (int): 두 번째 숫자
    Returns:
        int : 계산 결과
    
    >>> mul(10, 2)
    20
    """
    return a * b

def div(a, b):
    """나누기 함수
    Args:
        a (int): 첫 번째 숫자
        b (int): 두 번째 숫자
    Returns:
        int | float : 계산 결과
    
    >>> div(10, 2)
    5.0
    """
    return a / b

